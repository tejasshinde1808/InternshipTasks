public class ExampleLoop {
    public static void main(String[] args) {
        //for loop
        // for(int i=1;i<=100;i++){
        //     System.out.println(i+" ");
        // }
        
        // While loop
        int j=1;
        while (j<=100) {
            System.out.println(j+" ");
            j++;
        }
        
        //do-while
        int k=1;
        do {
            System.out.println(k+" ");
            k++;
        } while (k<=100);
    }
}
